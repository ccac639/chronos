import json

def create_dialogue(did, text, score, event, note, consequence, next_act):
    return {
        "id": did,
        "text": text,
        "speaker": "我",
        "avatar": "记",
        "score": score,
        "triggeredEvent": event,
        "historicalNote": note,
        "consequence": consequence,
        "nextActId": next_act
    }

def create_act(aid, title, is_key, time_marker, scene_desc, npcs, dialogues):
    return {
        "id": aid,
        "title": title,
        "isKeyAct": is_key,
        "timeMarker": time_marker,
        "sceneDescription": scene_desc,
        "npcDialogues": npcs,
        "dialogues": dialogues
    }

def create_npc(speaker, text, avatar):
    return {"speaker": speaker, "text": text, "avatar": avatar}

