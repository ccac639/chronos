acts = []

# ========== 辅助函数 ==========
def add_act(id, title, isKeyAct, timeMarker, sceneDesc, npcDialogues, dialogues):
    acts.append({
        "id": id,
        "title": title,
        "isKeyAct": isKeyAct,
        "timeMarker": timeMarker,
        "sceneDescription": sceneDesc,
        "npcDialogues": npcDialogues,
        "dialogues": dialogues
    })

def make_dialogue(did, text, score, triggeredEvent, historicalNote, consequence, nextActId):
    return {
        "id": did,
        "text": text,
        "speaker": "我",
        "avatar": "学",
        "score": score,
        "triggeredEvent": triggeredEvent,
        "historicalNote": historicalNote,
        "consequence": consequence,
        "nextActId": nextActId
    }

