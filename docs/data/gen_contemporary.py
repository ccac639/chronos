# Helper function to create dialogue
def make_dialogue(did, text, score, event, note, consequence, next_act):
    return {
        "id": did,
        "text": text,
        "speaker": "我",
        "avatar": "顾",
        "score": score,
        "triggeredEvent": event,
        "historicalNote": note,
        "consequence": consequence,
        "nextActId": next_act
    }

# Helper function to create act
def make_act(aid, title, is_key, time_marker, scene, npcs, dialogues):
    return {
        "id": aid,
        "title": title,
        "isKeyAct": is_key,
        "timeMarker": time_marker,
        "sceneDescription": scene,
        "npcDialogues": npcs,
        "dialogues": dialogues
    }

