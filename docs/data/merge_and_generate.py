import json

def make_act(id, title, isKeyAct, timeMarker, sceneDesc, npcs, dialogues):
    return {
        "id": id,
        "title": title,
        "isKeyAct": isKeyAct,
        "timeMarker": timeMarker,
        "sceneDescription": sceneDesc,
        "npcDialogues": npcs,
        "dialogues": dialogues
    }

def make_dial(id, text, score, event, note, conseq, nextId):
    return {
        "id": id,
        "text": text,
        "speaker": "我",
        "avatar": "外",
        "score": score,
        "triggeredEvent": event,
        "historicalNote": note,
        "consequence": conseq,
        "nextActId": nextId
    }

def npc(name, text, av):
    return {"speaker": name, "text": text, "avatar": av}

