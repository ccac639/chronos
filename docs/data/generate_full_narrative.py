def create_act(act_id, title, is_key, time_marker, scene_desc, npcs, dialogues):
    act = {
        "id": act_id,
        "title": title,
        "isKeyAct": is_key,
        "timeMarker": time_marker,
        "sceneDescription": scene_desc,
        "npcDialogues": [],
        "dialogues": []
    }
    for npc in npcs:
        act["npcDialogues"].append({
            "speaker": npc[0],
            "text": npc[1],
            "avatar": npc[2]
        })
    for i, d in enumerate(dialogues):
        act["dialogues"].append({
            "id": f"d{i+1}",
            "text": d[0],
            "speaker": "我",
            "avatar": "公",
            "score": d[1],
            "triggeredEvent": d[2],
            "historicalNote": d[3],
            "consequence": d[4],
            "nextActId": d[5]
        })
    return act

