def make_act(act_id, title, is_key, time_marker, scene_desc, npcs, dialogues):
    return {
        "id": act_id,
        "title": title,
        "isKeyAct": is_key,
        "timeMarker": time_marker,
        "sceneDescription": scene_desc,
        "npcDialogues": npcs,
        "dialogues": dialogues
    }

def make_dialogue(d_id, text, score, event, note, consequence, next_id):
    return {
        "id": d_id,
        "text": text,
        "speaker": "我",
        "avatar": "商",
        "score": score,
        "triggeredEvent": event,
        "historicalNote": note,
        "consequence": consequence,
        "nextActId": next_id
    }

