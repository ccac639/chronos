def d(did, text, score, trig, note, cons, nxt):
    return {
        "id": did, "text": text, "speaker": "我", "avatar": avatar,
        "score": score, "triggeredEvent": trig, "historicalNote": note,
        "consequence": cons, "nextActId": nxt
    }

def act(aid, title, is_key, time, scene, npcs, dialogues):
    return {
        "id": aid, "title": title, "isKeyAct": is_key,
        "timeMarker": time, "sceneDescription": scene,
        "npcDialogues": npcs, "dialogues": dialogues
    }

def npc(name, text, av):
    return {"speaker": name, "text": text, "avatar": av}

