import json

def make_dialogue(did, text, score, event, note, consequence, next_act, avatar="老"):
    return {
        "id": did,
        "text": text,
        "speaker": "我",
        "avatar": avatar,
        "score": score,
        "triggeredEvent": event,
        "historicalNote": note,
        "consequence": consequence,
        "nextActId": next_act
    }

def make_act(aid, title, is_key, time_marker, scene, npcs, dialogues):
    return {
        "id": aid,
        "title": title,
        "isKeyAct": is_key,
        "timeMarker": time_marker,
        "sceneDescription": scene,
        "npcDialogues": npcs,
        "dialogues": dialogues
    }

def npc(name, text, avatar):
    return {"speaker": name, "text": text, "avatar": avatar}

