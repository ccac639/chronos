def create_act(act_info):
    act = {
        "id": act_info["id"],
        "title": act_info["title"],
        "isKeyAct": act_info["isKeyAct"],
        "timeMarker": act_info["timeMarker"],
        "sceneDescription": act_info["sceneDescription"],
        "npcDialogues": [],
        "dialogues": []
    }
    for npc in act_info["npcs"]:
        act["npcDialogues"].append({
            "speaker": npc[0],
            "text": npc[1],
            "avatar": npc[2]
        })
    for i, d in enumerate(act_info["dialogues"]):
        act["dialogues"].append({
            "id": f"d{i+1}",
            "text": d[0],
            "speaker": "我",
            "avatar": "公",
            "score": d[1],
            "triggeredEvent": d[2],
            "historicalNote": d[3],
            "consequence": d[4],
            "nextActId": d[5]
        })
    return act

for act_info in acts_data:
    data["acts"].append(create_act(act_info))

