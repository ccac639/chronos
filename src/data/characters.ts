/** 获取指定时代的角色列表 */
export function getCharactersForEra(eraId: string): Character[] {
  const role = ALL_CHARACTER_ROLES.find((r) => r.eraId === eraId)
  return role?.characters ?? []
}

/** 获取全部角色数据 */
export function getAllCharacterRoles(): CharacterRole[] {
  return ALL_CHARACTER_ROLES
}

/** 随机获取指定时代的一个角色 */
export function getRandomCharacterForEra(eraId: string): Character | null {
  const chars = getCharactersForEra(eraId)
  if (chars.length === 0) return null
  return chars[Math.floor(Math.random() * chars.length)]
}
